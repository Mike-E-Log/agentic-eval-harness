"""End-to-end: the real driver wires next_phase -> run_phase -> run_gate ->
persist -> approve into a working run. The old version of this file asserted
`aeh run` was a no-op state write; it now asserts the loop actually drives."""
import os
import stat
import subprocess
import sys
import textwrap

from aeh.preflight import KEY_VARIANTS
from aeh.state_store import StateStore


def _git(*a, cwd):
    subprocess.run(["git", *a], cwd=cwd, check=True, capture_output=True)


def _init_repo(tmp_path):
    _git("init", cwd=tmp_path)
    _git("config", "user.email", "t@t", cwd=tmp_path)
    _git("config", "user.name", "t", cwd=tmp_path)
    (tmp_path / "f.txt").write_text("x", encoding="utf-8")
    _git("add", ".", cwd=tmp_path)
    _git("commit", "-m", "init", cwd=tmp_path)


def _phase_aware_fake_claude(tmp_path):
    """A fake `claude` that writes every phase's `docs/<PHASE>.md` on each call.

    It writes all five unconditionally rather than parsing the prompt: the
    Windows `.cmd` shim truncates a multi-line arg at the first newline, so a
    prompt-parsing fake would be fragile. The driver wiring is what's under
    test here; run_phase's claude invocation has its own tests."""
    d = tmp_path / "bin"
    d.mkdir(exist_ok=True)
    py = sys.executable.replace("\\", "/")
    script = d / "fake_claude.py"
    script.write_text(textwrap.dedent('''\
        import sys, pathlib
        for rel in ("docs/IDEATE.md", "docs/SPEC.md", "docs/PLAN.md",
                    "docs/IMPLEMENT.md", "docs/REVIEW.md"):
            p = pathlib.Path(rel)
            p.parent.mkdir(parents=True, exist_ok=True)
            if not p.exists():
                p.write_text("# " + rel + "\\n\\nGenerated by the e2e fake claude.\\n",
                             encoding="utf-8")
        sys.stdout.write("ok")
    '''), encoding="utf-8")
    name = "claude.cmd" if sys.platform == "win32" else "claude"
    p = d / name
    if sys.platform == "win32":
        p.write_text(f'@echo off\r\n"{py}" "{script}" %*\r\n', encoding="utf-8")
    else:
        p.write_text(f'#!/usr/bin/env bash\nexec "{py}" "{script}" "$@"\n', encoding="utf-8")
        p.chmod(p.stat().st_mode | stat.S_IEXEC)
    return str(d)


def _keyless_env(bindir):
    """A clean env: fake claude on PATH, every judge key removed so the gate
    runs empty (no network, hermetic) instead of making live API calls."""
    env = {**os.environ}
    for variants in KEY_VARIANTS.values():
        for v in variants:
            env.pop(v, None)
    env["PATH"] = bindir + os.pathsep + env.get("PATH", "")
    env["NO_COLOR"] = "1"
    return env


def test_run_cli_drives_all_five_phases_to_completion(tmp_path):
    _init_repo(tmp_path)
    bindir = _phase_aware_fake_claude(tmp_path)
    env = _keyless_env(bindir)

    r = subprocess.run([sys.executable, "-m", "aeh", "run", ".", "--yes"],
                       cwd=tmp_path, capture_output=True, text=True,
                       encoding="utf-8", env=env)
    assert r.returncode == 0, f"stdout={r.stdout}\nstderr={r.stderr}"

    runs = list((tmp_path / ".aeh" / "runs").iterdir())
    assert len(runs) == 1
    rid = runs[0].name
    state = StateStore(tmp_path, rid).read()
    assert state["status"] == "completed", state
    assert state["phase"] == "review"                 # advanced through all five
    assert state["last_gate"]["phase"] == "review"    # a gate fired at the last boundary

    wt = tmp_path / ".aeh" / "worktrees" / rid
    for doc in ("IDEATE.md", "SPEC.md", "PLAN.md", "IMPLEMENT.md", "REVIEW.md"):
        assert (wt / "docs" / doc).is_file(), f"{doc} not written by phase driver"


def test_drive_fires_gate_with_scores_and_advances(tmp_path, monkeypatch):
    from aeh import driver
    from aeh.eval_gate.judging import JudgeOutput

    _init_repo(tmp_path)
    bindir = _phase_aware_fake_claude(tmp_path)
    monkeypatch.setenv("PATH", bindir + os.pathsep + os.environ["PATH"])
    # keyless gate scoring: canned judge outputs, no network
    monkeypatch.setattr("aeh.eval_gate.judging.score_candidate",
                        lambda judge, **kw: JudgeOutput(judge, 8.0, f"{judge} ok"))

    calls = {"n": 0}

    def approve(phase, gate):
        calls["n"] += 1
        return calls["n"] < 2          # approve after ideate, halt after spec

    state = driver.drive(tmp_path, "rid1", judges=["claude", "gpt"],
                         approve=approve, timeout=60, emit=lambda *a, **k: None)

    assert state["phase"] == "spec"               # ideate -> approved -> spec
    assert state["status"] == "gated"
    assert state["last_gate"]["phase"] == "spec"
    scores = state["last_gate"]["scores"]
    assert len(scores) == 2 and all(s["score"] == 8.0 for s in scores)
    assert calls["n"] == 2                         # halted at the spec boundary
    # state persisted for `aeh resume`
    assert StateStore(tmp_path, "rid1").read()["last_gate"]["phase"] == "spec"
